package  com.example.mop125;


public class ItemModel {
    private int image;
    private String num;

    public ItemModel() {
    }

    public ItemModel(int image, String num) {
        this.image = image;
        this.num = num;
    }

    public int getImage() {
        return image;
    }

    public String getNum() {
        return num;
    }

}
